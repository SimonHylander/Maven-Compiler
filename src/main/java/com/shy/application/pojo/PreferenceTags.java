package com.shy.application.pojo;

public class PreferenceTags {
	String innertag;
	String name;
	String value;
	boolean consolewindow;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public boolean isConsolewindow() {
		return consolewindow;
	}
	public void setConsolewindow(boolean consolewindow) {
		this.consolewindow = consolewindow;
	}
	public String getInnertag() {
		return innertag;
	}
	public void setInnertag(String innertag) {
		this.innertag = innertag;
	}
	
	
}
